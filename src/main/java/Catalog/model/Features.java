package Catalog.model;

public class Features {
    public int Id;
    public String Type;
    public int Market;

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }

    public int getMarket() {
        return Market;
    }

    public void setMarket(int market) {
        Market = market;
    }
}
